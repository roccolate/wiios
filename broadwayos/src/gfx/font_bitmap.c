#include "font_bitmap.h"

static char up(char c) {
  if (c >= 'a' && c <= 'z') return (char)(c - ('a' - 'A'));
  return c;
}

static const unsigned char *glyph(char c) {
  static const unsigned char q[7] = {0x0E,0x11,0x02,0x04,0x04,0x00,0x04};
  static const unsigned char sp[7]= {0,0,0,0,0,0,0};
  static const unsigned char dash[7]={0,0,0,0x1F,0,0,0};
  static const unsigned char dot[7] = {0,0,0,0,0,0x0C,0x0C};
  static const unsigned char colon[7]={0,0x0C,0x0C,0,0x0C,0x0C,0};
  static const unsigned char slash[7]={0x01,0x02,0x04,0x08,0x10,0,0};

  static const unsigned char n0[7]={0x0E,0x11,0x13,0x15,0x19,0x11,0x0E};
  static const unsigned char n1[7]={0x04,0x0C,0x04,0x04,0x04,0x04,0x0E};
  static const unsigned char n2[7]={0x0E,0x11,0x01,0x02,0x04,0x08,0x1F};
  static const unsigned char n3[7]={0x1E,0x01,0x01,0x06,0x01,0x01,0x1E};
  static const unsigned char n4[7]={0x02,0x06,0x0A,0x12,0x1F,0x02,0x02};
  static const unsigned char n5[7]={0x1F,0x10,0x1E,0x01,0x01,0x11,0x0E};
  static const unsigned char n6[7]={0x06,0x08,0x10,0x1E,0x11,0x11,0x0E};
  static const unsigned char n7[7]={0x1F,0x01,0x02,0x04,0x08,0x08,0x08};
  static const unsigned char n8[7]={0x0E,0x11,0x11,0x0E,0x11,0x11,0x0E};
  static const unsigned char n9[7]={0x0E,0x11,0x11,0x0F,0x01,0x02,0x0C};

  static const unsigned char A[7]={0x0E,0x11,0x11,0x1F,0x11,0x11,0x11};
  static const unsigned char B[7]={0x1E,0x11,0x11,0x1E,0x11,0x11,0x1E};
  static const unsigned char C[7]={0x0E,0x11,0x10,0x10,0x10,0x11,0x0E};
  static const unsigned char D[7]={0x1C,0x12,0x11,0x11,0x11,0x12,0x1C};
  static const unsigned char E[7]={0x1F,0x10,0x10,0x1E,0x10,0x10,0x1F};
  static const unsigned char F[7]={0x1F,0x10,0x10,0x1E,0x10,0x10,0x10};
  static const unsigned char G[7]={0x0E,0x11,0x10,0x10,0x13,0x11,0x0E};
  static const unsigned char H[7]={0x11,0x11,0x11,0x1F,0x11,0x11,0x11};
  static const unsigned char I[7]={0x0E,0x04,0x04,0x04,0x04,0x04,0x0E};
  static const unsigned char J[7]={0x01,0x01,0x01,0x01,0x11,0x11,0x0E};
  static const unsigned char K[7]={0x11,0x12,0x14,0x18,0x14,0x12,0x11};
  static const unsigned char L[7]={0x10,0x10,0x10,0x10,0x10,0x10,0x1F};
  static const unsigned char M[7]={0x11,0x1B,0x15,0x11,0x11,0x11,0x11};
  static const unsigned char N[7]={0x11,0x11,0x19,0x15,0x13,0x11,0x11};
  static const unsigned char O[7]={0x0E,0x11,0x11,0x11,0x11,0x11,0x0E};
  static const unsigned char P[7]={0x1E,0x11,0x11,0x1E,0x10,0x10,0x10};
  static const unsigned char R[7]={0x1E,0x11,0x11,0x1E,0x14,0x12,0x11};
  static const unsigned char S[7]={0x0F,0x10,0x10,0x0E,0x01,0x01,0x1E};
  static const unsigned char T[7]={0x1F,0x04,0x04,0x04,0x04,0x04,0x04};
  static const unsigned char U[7]={0x11,0x11,0x11,0x11,0x11,0x11,0x0E};
  static const unsigned char V[7]={0x11,0x11,0x11,0x11,0x11,0x0A,0x04};
  static const unsigned char W[7]={0x11,0x11,0x11,0x11,0x15,0x1B,0x11};
  static const unsigned char X[7]={0x11,0x11,0x0A,0x04,0x0A,0x11,0x11};
  static const unsigned char Y[7]={0x11,0x11,0x0A,0x04,0x04,0x04,0x04};
  static const unsigned char Z[7]={0x1F,0x01,0x02,0x04,0x08,0x10,0x1F};

  c = up(c);
  if (c == ' ') return sp;
  if (c == '-') return dash;
  if (c == '.') return dot;
  if (c == ':') return colon;
  if (c == '/') return slash;
  if (c >= '0' && c <= '9') {
    static const unsigned char *nums[10] = {n0,n1,n2,n3,n4,n5,n6,n7,n8,n9};
    return nums[c - '0'];
  }
  switch (c) {
    case 'A': return A; case 'B': return B; case 'C': return C; case 'D': return D;
    case 'E': return E; case 'F': return F; case 'G': return G; case 'H': return H;
    case 'I': return I; case 'J': return J; case 'K': return K; case 'L': return L;
    case 'M': return M; case 'N': return N; case 'O': return O; case 'P': return P;
    case 'R': return R; case 'S': return S; case 'T': return T; case 'U': return U;
    case 'V': return V; case 'W': return W; case 'X': return X; case 'Y': return Y;
    case 'Z': return Z;
    default: return q;
  }
}

static void draw_glyph(WiiSurface *s, wii_i32 x, wii_i32 y, const unsigned char *g, wii_u32 color, wii_i32 scale) {
  for (wii_i32 row = 0; row < 7; ++row) {
    for (wii_i32 col = 0; col < 5; ++col) {
      if ((g[row] >> (4 - col)) & 1U) {
        surface_fill_rect(s, (WiiRect){x + col * scale, y + row * scale, scale, scale}, color);
      }
    }
  }
}

void font_draw_text(WiiSurface *s, wii_i32 x, wii_i32 y, const char *text, wii_u32 color, wii_i32 scale) {
  if (!s || !text || scale <= 0) return;
  while (*text) {
    draw_glyph(s, x, y, glyph(*text), color, scale);
    x += 6 * scale;
    ++text;
  }
}

wii_i32 font_text_width(const char *text, wii_i32 scale) {
  wii_i32 n = 0;
  if (!text || scale <= 0) return 0;
  while (text[n]) ++n;
  return n * 6 * scale;
}
